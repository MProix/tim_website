            <h3> Seizure dynamics and forecasting</h3>

            <p>Neural activity of people with epilepsy is typically organized on several temporal and spatial scales. 
                Using tools borrowed from dynamical systems theory and machine/statistical learning, 
                we try to understand and model those dynamics. Our goal is to leverage those findings 
                to offer effective diagnoses and forecasting tools for people with epilepsy.</p>

